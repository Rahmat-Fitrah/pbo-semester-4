public class Number {
    public static void main(String[] args) {
        //6. Adding Numbers and Strings
        int x = 100;
        int y = 20;
        int z = x * y;  // 200o
        System.out.println("Hasil : " +z);
        
        String A = "10";
        String B = "20";
        String Y = A + B;  // z will be 1020 (a String)
        System.out.println("Hasil : " +Y);
    }
}
