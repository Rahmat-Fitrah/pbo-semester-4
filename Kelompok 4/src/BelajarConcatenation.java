public class BelajarConcatenation {
    public static void main(String[] args) {
        //4 Fungsi String Concatenation
        String Nama = "John ";
        String Panggilan = "Doe";
        System.out.println(Nama.concat(Panggilan)+"\n");

        // 5 fungsi Concatenation in Sentences
        String name = "John";
        int age = 25;
        System.out.println("My name is " + name + " and I am " + age + " years old.");
    }
}
